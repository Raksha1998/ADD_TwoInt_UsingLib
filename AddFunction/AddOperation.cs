using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AddFunction
{
    public class AddOperation
    {
        public static int Add(int a, int b)
        {
            int c = a + b;
            return c;
        }

        public static int Sub(int a, int b)
        {
            int c = a - b;
            return c;
        }
    }
